let viewportX = 0;
let viewportY = 0;

export function getViewportX(): number {
    return viewportX;
}

export function getViewportY(): number {
    return viewportY;
}

export function setViewportX(x: number): void {
    viewportX = x;
}

export function setViewportY(y: number): void {
    viewportY = y;
}
